//---------------------------------------------------------------------------------------------------------------------*

import Cocoa

//---------------------------------------------------------------------------------------------------------------------*

@objc(PMButton) class PMButton : NSButton, PMUserClassName {

  //-------------------------------------------------------------------------------------------------------------------*

  func userClassName () -> String { return "PMButton" }
 
  //-------------------------------------------------------------------------------------------------------------------*

  required init (coder: NSCoder!) {
    super.init (coder:coder)
    noteObjectAllocation (self)
  }

  //-------------------------------------------------------------------------------------------------------------------*

  deinit {
    noteObjectDeallocation (self)
  }

}

//---------------------------------------------------------------------------------------------------------------------*
