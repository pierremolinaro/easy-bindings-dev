//---------------------------------------------------------------------------------------------------------------------*

import Cocoa

//---------------------------------------------------------------------------------------------------------------------*

@objc(PMMatrix) class PMMatrix : NSMatrix, PMUserClassName {

  //-------------------------------------------------------------------------------------------------------------------*

  func userClassName () -> String { return "PMMatrix" }
 
  //-------------------------------------------------------------------------------------------------------------------*

  required init (coder: NSCoder!) {
    super.init (coder:coder)
    noteObjectAllocation (self)
  }

  //-------------------------------------------------------------------------------------------------------------------*

  deinit {
    noteObjectDeallocation (self)
  }

}

//---------------------------------------------------------------------------------------------------------------------*
