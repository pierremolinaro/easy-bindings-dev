//---------------------------------------------------------------------------------------------------------------------*

import Cocoa

//---------------------------------------------------------------------------------------------------------------------*

@objc(PMSwitch) class PMSwitch : NSButton, PMUserClassName, PMEnableProtocol {

  //-------------------------------------------------------------------------------------------------------------------*

  func userClassName () -> String { return "PMSwitch" }
 
  //-------------------------------------------------------------------------------------------------------------------*

  required init (coder: NSCoder!) {
    super.init (coder:coder)
    noteObjectAllocation (self)
  }

  //-------------------------------------------------------------------------------------------------------------------*

  deinit {
    noteObjectDeallocation (self)
  }
  
  //-------------------------------------------------------------------------------------------------------------------*

  func setEnableFromBinding (flag : Bool) {
    self.enabled = flag
  }

}

//---------------------------------------------------------------------------------------------------------------------*
