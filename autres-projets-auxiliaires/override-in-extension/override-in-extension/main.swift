import Foundation

//——————————————————————————————————————————————————————————————————————————————————————————————————————————————————————

class A {
  func maFonction () {
    print ("A::\(#function)")
  }
}

//——————————————————————————————————————————————————————————————————————————————————————————————————————————————————————

class B : A {
}

//——————————————————————————————————————————————————————————————————————————————————————————————————————————————————————

extension B {
  override func maFonction () {
    print ("B::\(#function)")
  }
}

//——————————————————————————————————————————————————————————————————————————————————————————————————————————————————————

let x = B ()
x.maFonction()

