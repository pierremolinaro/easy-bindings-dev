import Foundation

//——————————————————————————————————————————————————————————————————————————————————————————————————————————————————————

class A : NSObject {
  @objc dynamic func myFonction () {
    print ("A::\(#function)")
  }
}

//——————————————————————————————————————————————————————————————————————————————————————————————————————————————————————

extension A {
  @objc func otherFonction () {
    print ("A::\(#function)")
  }
}

//——————————————————————————————————————————————————————————————————————————————————————————————————————————————————————

class B : A {
}

//——————————————————————————————————————————————————————————————————————————————————————————————————————————————————————

extension B {
  @objc override func otherFonction () {
    print ("B::\(#function)")
  }
}

//——————————————————————————————————————————————————————————————————————————————————————————————————————————————————————

class C : B {
}

//——————————————————————————————————————————————————————————————————————————————————————————————————————————————————————

extension C {
  override func myFonction () {
    print ("C::\(#function)")
  }
}

//——————————————————————————————————————————————————————————————————————————————————————————————————————————————————————
//   MAIN
//——————————————————————————————————————————————————————————————————————————————————————————————————————————————————————

let x = C ()
x.myFonction()
x.otherFonction()

